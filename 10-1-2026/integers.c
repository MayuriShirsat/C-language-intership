#include<stdio.h>
int main(){
	int a=12;//positive integer
	int b=-12;//negative integer
	int c=0;//zero
	int d=012;//octal integer
    int e=0x1A;//hexadecimal integer
    
    printf("Positive Integer: %d\n",a);
    printf("Negative Integer: %d\n",b);
    printf("Zero Integer: %d\n",c);
    printf("Octal Integer: %d\n",d);
    printf("Hexadecimal Integer: %d\n",e);
}
